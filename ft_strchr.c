char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	str = (char*)s;
	i = 0;
	while (str[i] || str[i] == c)
	{
		if (str[i] == c)
			return (&str[i]);
		i++;
	}
	return (0);
}
